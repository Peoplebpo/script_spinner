function mostrar() {
            div = document.getElementById('flotante');
            div.style.display = '';

            div = document.getElementById('inicial');
            div.style.display = 'none';
        }

        function cerrar() {
            div = document.getElementById('flotante');
            div.style.display = 'none';

            div = document.getElementById('inicial');
            div.style.display = '';
        }